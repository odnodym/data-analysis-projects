valid_columns = list(questions.intersection(columns))

full_answers = df[valid_columns].dropna(how='any')

print("Respondents with full answers:", len(full_answers))

"""**Вывод:** В датасете нет респондентов, которые ответили на все вопросы. Это связано с большим количеством вопросов и пропущенных значений."""

# Задание 3 — статистика WorkExp

work_exp = df['WorkExp'].dropna()

print("Mean:", work_exp.mean())
