import pandas as pd

df = pd.read_csv('survey_results_public.csv', low_memory=False, encoding='utf-8', on_bad_lines='skip')
schema = pd.read_csv('survey_results_schema.csv', encoding='utf-8')

df.head()

# Задание 1 — количество респондентов
total_respondents = len(df)

