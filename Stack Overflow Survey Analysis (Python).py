"""**Вывод:** Средний опыт работы составляет около 13 лет, медианное и наиболее частое значение — 10 лет. Это говорит о том, что большинство респондентов имеют около 10 лет опыта."""

# Задание 4 — удалённая работа

df['RemoteWork'].value_counts()

