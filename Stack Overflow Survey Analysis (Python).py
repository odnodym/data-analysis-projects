# Задание 7 — Географический анализ зарплат Python-разработчиков

# фильтруем Python-разработчиков
python_dev = df[df['LanguageHaveWorkedWith'].str.contains('Python', na=False)]

# группируем по странам и считаем среднее и медиану
salary_stats = python_dev.groupby('Country')['ConvertedCompYearly'].agg(['mean', 'median'])

# убираем пропуски
salary_stats = salary_stats.dropna()

# округляем и сортируем
salary_stats = salary_stats.round(2).sort_values(by='mean', ascending=False)

salary_stats.head()

"""**Вывод:** Уровень заработной платы Python-разработчиков значительно отличается в зависимости от страны. В некоторых странах наблюдается большой разрыв между средним и медианным значением, что может указывать на неравномерное распределение доходов."""

# Задание 8 — топ 5 по зарплате

salary_df = df[['ConvertedCompYearly', 'EdLevel']].dropna(subset=['ConvertedCompYearly'])

top5_education = salary_df.sort_values(by='ConvertedCompYearly', ascending=False).head(5)

top5_education

"""**Вывод:** Среди самых высокооплачиваемых респондентов встречаются как бакалавры,
так и специалисты с магистерским образованием. Это показывает, что высокий доход возможен при разных уровнях образования.
"""

# Задание 9 — Анализ популярности Python по возрастным группам

# создаём копию DataFrame (чтобы избежать SettingWithCopyWarning)
df_copy = df[['Age', 'LanguageHaveWorkedWith']].copy()

# создаём столбец с признаком использования Python
df_copy['UsesPython'] = df_copy['LanguageHaveWorkedWith'].str.contains('Python', na=False)

# группируем по возрасту и считаем процент
python_by_age = df_copy.groupby('Age')['UsesPython'].mean() * 100

# округляем значения
python_by_age = python_by_age.round(2)

python_by_age

"""**Вывод:** Python наиболее популярен среди молодых специалистов (18–24 года), где его используют около 40% респондентов. С увеличением возраста популярность Python постепенно снижается."""

# Задание 10 — индустрии топ-remote

# считаем 75 перцентиль
threshold = df['ConvertedCompYearly'].quantile(0.75)

# фильтруем: зарплата выше порога + удалёнка
filtered = df[
    (df['ConvertedCompYearly'] > threshold) &
    (df['RemoteWork'] == 'Remote')
]

# убираем пропуски в индустрии
filtered = filtered.dropna(subset=['Industry'])

# считаем популярные индустрии
top_industries = filtered['Industry'].value_counts()

top_industries

"""**Вывод:** Среди высокооплачиваемых удалённых специалистов наиболее популярной индустрией является Software Development. Также значительную долю занимают Fintech и Healthcare. Это показывает, что IT-сфера остаётся лидером по уровню доходов среди remote специалистов."""